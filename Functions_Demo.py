# A function is a block of code that can run whenever it is called.
# Information passed to a function is called an argument.

#FUNCTION DEFINITION: Using a variable

def squared(x):
    return x**2

#FUNCTION CALL: Called by passing values/numbers/integers as parameter here

print(squared(2))
print(squared(4))

#Output: The following lines were the output and should be considered as comments.
4
16

